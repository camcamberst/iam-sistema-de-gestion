const TEST_USERS = [
  {
    name: 'Test Usuario Sede MP',
    email: 'test.sedemp@example.com',
    role: 'modelo',
    group: 'Sede MP',
    expectedRoom: true,
    expectedJornada: true,
    description: 'Usuario normal con Room y Jornada requeridos'
  },
  {
    name: 'Test Usuario Sat√©lites',
    email: 'test.satelites@example.com', 
    role: 'modelo',
    group: 'Sat√©lites',
    expectedRoom: false, // NO requerido
    expectedJornada: false, // Opcional
    description: 'Usuario Sat√©lites - Room NO requerido, Jornada opcional'
  },
  {
    name: 'Test Usuario Otros',
    email: 'test.otros@example.com',
    role: 'modelo', 
    group: 'Otros',
    expectedRoom: false, // Opcional
    expectedJornada: false, // Opcional
    description: 'Usuario Otros - Room y Jornada opcionales'
  }
];

// =====================================================
// üß™ PRUEBAS ESPEC√çFICAS
// =====================================================

async function testCrearEditarUsuarios() {
  console.log('üöÄ INICIANDO PRUEBAS DE CREAR Y EDITAR USUARIOS');
  console.log('=' .repeat(60));
  
  let testsPassed = 0;
  let totalTests = 0;
  
  // =====================================================
  // 1. OBTENER DATOS NECESARIOS
  // =====================================================
  
  console.log('\nüìä 1. OBTENIENDO DATOS DEL SISTEMA');
  console.log('-'.repeat(40));
  
  // Obtener grupos
  const groupsResponse = await makeRequest(`${BASE_URL}/api/groups`);
  if (!groupsResponse.success) {
    console.log('‚ùå No se pudieron obtener los grupos');
    return;
  }
  
  const groups = groupsResponse.data.groups;
  console.log(`‚úÖ Grupos obtenidos: ${groups.length}`);
  
  // Obtener rooms por grupo
  const roomsByGroup = {};
  for (const group of groups) {
    const roomsResponse = await makeRequest(`${BASE_URL}/api/groups/rooms?groupId=${group.id}`);
    if (roomsResponse.success) {
      roomsByGroup[group.name] = roomsResponse.data.rooms || [];
      console.log(`   ${group.name}: ${roomsByGroup[group.name].length} rooms`);
    }
  }
  
  // =====================================================
  // 2. PRUEBAS DE CREACI√ìN DE USUARIOS
  // =====================================================
  
  console.log('\nüë§ 2. PRUEBAS DE CREACI√ìN DE USUARIOS');
  console.log('-'.repeat(40));
  
  const createdUsers = [];
  
  for (const testUser of TEST_USERS) {
    console.log(`\nüß™ Probando: ${testUser.description}`);
    
    // Encontrar el grupo
    const group = groups.find(g => g.name === testUser.group);
    if (!group) {
      console.log(`‚ùå Grupo ${testUser.group} no encontrado`);
      continue;
    }
    
    // Preparar datos del usuario
    const userData = {
      name: testUser.name,
      email: testUser.email,
      password: 'TestPassword123!',
      role: testUser.role,
      group_ids: [group.id],
      is_active: true
    };
    
    // Agregar Room y Jornada si est√°n disponibles
    const availableRooms = roomsByGroup[testUser.group] || [];
    if (availableRooms.length > 0) {
      userData.room_id = availableRooms[0].id;
      userData.jornada = 'MA√ëANA';
    }
    
    // Test: Crear usuario
    totalTests++;
    const createResponse = await makeRequest(`${BASE_URL}/api/users`, {
      method: 'POST',
      body: JSON.stringify(userData)
    });
    
    if (logTest(`Crear usuario ${testUser.name}`, createResponse)) {
      testsPassed++;
      if (createResponse.data?.user) {
        createdUsers.push({
          ...testUser,
          id: createResponse.data.user.id,
          created: true
        });
        console.log(`   Usuario creado con ID: ${createResponse.data.user.id}`);
      }
    }
  }
  
  // =====================================================
  // 3. PRUEBAS DE EDICI√ìN DE USUARIOS
  // =====================================================
  
  console.log('\n‚úèÔ∏è 3. PRUEBAS DE EDICI√ìN DE USUARIOS');
  console.log('-'.repeat(40));
  
  for (const user of createdUsers) {
    if (!user.created) continue;
    
    console.log(`\nüß™ Editando: ${user.name}`);
    
    // Obtener asignaciones actuales
    totalTests++;
    const assignmentsResponse = await makeRequest(`${BASE_URL}/api/assignments/${user.id}`);
    
    if (logTest(`Obtener asignaciones de ${user.name}`, assignmentsResponse)) {
      testsPassed++;
      if (assignmentsResponse.data?.assignments) {
        console.log(`   Asignaciones encontradas: ${assignmentsResponse.data.assignments.length}`);
        assignmentsResponse.data.assignments.forEach(assignment => {
          console.log(`     - Room: ${assignment.room_name || 'N/A'}, Jornada: ${assignment.jornada || 'N/A'}`);
        });
      }
    }
    
    // Preparar datos de actualizaci√≥n
    const updateData = {
      id: user.id,
      name: user.name + ' (Editado)',
      email: user.email,
      password: '', // No cambiar contrase√±a
      role: user.role,
      group_ids: user.group_ids || [],
      is_active: true
    };
    
    // Agregar Room y Jornada si est√°n disponibles
    const availableRooms = roomsByGroup[user.group] || [];
    if (availableRooms.length > 0) {
      updateData.room_id = availableRooms[0].id;
      updateData.jornada = 'TARDE'; // Cambiar jornada
    }
    
    // Test: Actualizar usuario
    totalTests++;
    const updateResponse = await makeRequest(`${BASE_URL}/api/users`, {
      method: 'PUT',
      body: JSON.stringify(updateData)
    });
    
    if (logTest(`Actualizar usuario ${user.name}`, updateResponse)) {
      testsPassed++;
      console.log(`   Usuario actualizado exitosamente`);
    }
  }
  
  // =====================================================
  // 4. PRUEBAS DE VALIDACI√ìN POR GRUPO
  // =====================================================
  
  console.log('\n‚öôÔ∏è 4. PRUEBAS DE VALIDACI√ìN POR GRUPO');
  console.log('-'.repeat(40));
  
  for (const user of createdUsers) {
    if (!user.created) continue;
    
    console.log(`\nüß™ Validando configuraci√≥n para: ${user.name} (${user.group})`);
    
    // Verificar si el usuario tiene Room y Jornada seg√∫n las reglas del grupo
    totalTests++;
    const assignmentsResponse = await makeRequest(`${BASE_URL}/api/assignments/${user.id}`);
    
    if (assignmentsResponse.success && assignmentsResponse.data?.assignments) {
      const hasRoom = assignmentsResponse.data.assignments.some(a => a.room_id);
      const hasJornada = assignmentsResponse.data.assignments.some(a => a.jornada);
      
      let validationPassed = true;
      
      // Validar seg√∫n las reglas del grupo
      if (user.group === 'Sat√©lites') {
        // Sat√©lites: Room NO requerido, Jornada opcional
        if (hasRoom) {
          console.log(`   ‚ö†Ô∏è  Sat√©lites tiene Room asignado (deber√≠a ser opcional)`);
        }
        console.log(`   ‚úÖ Sat√©lites: Room=${hasRoom ? 'S√≠' : 'No'}, Jornada=${hasJornada ? 'S√≠' : 'No'}`);
      } else if (user.group === 'Otros') {
        // Otros: Room y Jornada opcionales
        console.log(`   ‚úÖ Otros: Room=${hasRoom ? 'S√≠' : 'No'}, Jornada=${hasJornada ? 'S√≠' : 'No'}`);
      } else {
        // Otros grupos: Room y Jornada requeridos
        if (!hasRoom || !hasJornada) {
          console.log(`   ‚ö†Ô∏è  ${user.group} deber√≠a tener Room y Jornada requeridos`);
          validationPassed = false;
        } else {
          console.log(`   ‚úÖ ${user.group}: Room=S√≠, Jornada=S√≠`);
        }
      }
      
      if (logTest(`Validaci√≥n de configuraci√≥n ${user.group}`, { success: validationPassed })) {
        testsPassed++;
      }
    } else {
      logTest(`Validaci√≥n de configuraci√≥n ${user.group}`, { success: false }, 'No se pudieron obtener asignaciones');
    }
  }
  
  // =====================================================
  // 5. LIMPIEZA: ELIMINAR USUARIOS DE PRUEBA
  // =====================================================
  
  console.log('\nüßπ 5. LIMPIEZA: ELIMINAR USUARIOS DE PRUEBA');
  console.log('-'.repeat(40));
  
  for (const user of createdUsers) {
    if (!user.created) continue;
    
    totalTests++;
    const deleteResponse = await makeRequest(`${BASE_URL}/api/users`, {
      method: 'DELETE',
      body: JSON.stringify({ id: user.id })
    });
    
    if (logTest(`Eliminar usuario ${user.name}`, deleteResponse)) {
      testsPassed++;
    }
  }
  
  // =====================================================
  // üìä RESUMEN DE PRUEBAS
  // =====================================================
  
  console.log('\n' + '='.repeat(60));
  console.log('üìä RESUMEN DE PRUEBAS DE CREAR/EDITAR USUARIOS');
  console.log('='.repeat(60));
  console.log(`‚úÖ Pruebas exitosas: ${testsPassed}/${totalTests}`);
  console.log(`‚ùå Pruebas fallidas: ${totalTests - testsPassed}/${totalTests}`);
  console.log(`üìà Porcentaje de √©xito: ${Math.round((testsPassed / totalTests) * 100)}%`);
  
  // =====================================================
  // üéØ AN√ÅLISIS DE RESULTADOS
  // =====================================================
  
  console.log('\nüéØ AN√ÅLISIS DE RESULTADOS:');
  console.log('-'.repeat(40));
  
  if (testsPassed === totalTests) {
    console.log('‚úÖ Todas las pruebas de crear/editar usuarios pasaron');
    console.log('üìã Sistema listo para configuraciones especiales por grupo');
  } else {
    console.log('‚ö†Ô∏è  Algunas pruebas fallaron');
    console.log('üìã Revisar errores antes de continuar');
  }
  
  console.log('\nüöÄ Pruebas de crear/editar usuarios completadas');
}

